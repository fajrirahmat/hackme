package com.sinauacademy.hackme.bean;

public class Invoice {
	private String noFaktur;
	private long tglFaktur;
	private String lastUser;
	private String dariKepada;
	private long lastUpdate;
	private String keterangan;
	public String getNoFaktur() {
		return noFaktur;
	}
	public void setNoFaktur(String noFaktur) {
		this.noFaktur = noFaktur;
	}
	public long getTglFaktur() {
		return tglFaktur;
	}
	public void setTglFaktur(long tglFaktur) {
		this.tglFaktur = tglFaktur;
	}
	public String getLastUser() {
		return lastUser;
	}
	public void setLastUser(String lastUser) {
		this.lastUser = lastUser;
	}
	public String getDariKepada() {
		return dariKepada;
	}
	public void setDariKepada(String dariKepada) {
		this.dariKepada = dariKepada;
	}
	public long getLastUpdate() {
		return lastUpdate;
	}
	public void setLastUpdate(long lastUpdate) {
		this.lastUpdate = lastUpdate;
	}
	public String getKeterangan() {
		return keterangan;
	}
	public void setKeterangan(String keterangan) {
		this.keterangan = keterangan;
	}
	
	
	
}
